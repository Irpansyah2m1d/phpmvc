class OperatorBit {
    
     public static void main(String[] args) {
         System.out.println("\n========================");
         System.out.println("===== Soal Latihan ===== \n");
         // Soal No 1
        System.out.println("1. 23 & 02 = " + (23 & 02));
         // Soal No 2
        System.out.println("2. 05 & 12 = " + (05 & 12));
         // Soal No 3
        System.out.println("3. 10 | 03 = " + (10 | 03));
         // Soal No 4
        System.out.println("4. 15 | 04 = " + (15 | 04));
         // Soal No 5
        System.out.println("5. 25 ^ 12 = " + (25 ^ 12));
         // Soal No 6
        System.out.println("6. 20 ^ 06 = " + (20 ^ 06));
         // Soal No 7
        System.out.println("7. 16 << 1 = " + (16 << 1));
         // Soal No 8
        System.out.println("8. 16 << 2 = " + (16 << 2));
         // Soal No 9
        System.out.println("9. 14 >> 1 = " + (14 >> 1));
         // Soal No 10
        System.out.println("10. 07 >> 2 = " + (07 >> 2));
    }
}